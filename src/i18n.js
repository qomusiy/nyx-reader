export const LANGS = [
  { code: "uz", label: "O'zbekcha" },
  { code: "en", label: "English" },
  { code: "ru", label: "Русский" },
];

let lang = localStorage.getItem("nyx-lang");
if (!STR[lang]) lang = "uz";

const listeners = new Set();

export function getLang() { return lang; }

export function t(key, vars) {
  const dict = STR[lang] || STR.uz;
  let s = dict[key] ?? STR.uz[key] ?? key;
  if (vars) for (const k in vars) s = s.replace(new RegExp(`{${k}}`, "g"), String(vars[k]));
  return s;
}

// Subscribe to language changes; returns an unsubscribe function.
export function onLangChange(fn) { listeners.add(fn); return () => listeners.delete(fn); }

export function setLang(next) {
  if (!STR[next] || next === lang) return;
  lang = next;
  localStorage.setItem("nyx-lang", lang);
  document.documentElement.lang = lang;
  applyI18n(document);
  listeners.forEach((fn) => { try { fn(lang); } catch (e) { console.error(e); } });
}

// Translate all tagged elements within root.
export function applyI18n(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((el) => { el.textContent = t(el.dataset.i18n); });
  root.querySelectorAll("[data-i18n-ph]").forEach((el) => { el.setAttribute("placeholder", t(el.dataset.i18nPh)); });
  root.querySelectorAll("[data-i18n-title]").forEach((el) => {
    const s = t(el.dataset.i18nTitle);
    el.setAttribute("title", s);
    el.setAttribute("aria-label", s);
  });
  root.querySelectorAll("[data-i18n-aria]").forEach((el) => { el.setAttribute("aria-label", t(el.dataset.i18nAria)); });
}
